% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
format long;
l = str2double(get(handles.edit1, 'String'));
p = str2double(get(handles.edit2, 'String'));
t0 = str2double(get(handles.edit3, 'String'));
Q = str2double(get(handles.edit4, 'String'));
c = str2double(get(handles.edit5, 'String'));
a = str2double(get(handles.edit6, 'String'));
b = str2double(get(handles.edit7, 'String'));
sigma = str2double(get(handles.edit8, 'String'));
u0 = str2double(get(handles.edit9, 'String'));
h = str2double(get(handles.edit12, 'String'));
tau = str2double(get(handles.edit13, 'String'));
T = str2double(get(handles.edit14, 'String'));
[U, N] = getU(l, T, p, t0, Q, c, a, b, sigma, u0, h, tau);
[maxT, max_j] = max(U(:, floor(N/2))); 
MaxT = num2str(maxT, 10);
set(handles.text7, 'String', MaxT);
columnformat = cell(0,1);
for i = 1:N
    columnformat{end+1} = 'long';
end
set(handles.table,'Data', U, 'ColumnFormat', columnformat)

